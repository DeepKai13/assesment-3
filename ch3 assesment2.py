import math


num=int(input('Enter your number>>>>>>: ')) 
result_sqrt = math.sqrt(num) 
print('Square root of ',  num , ' is >>>',  result_sqrt)
 
 
result_sine = math.sin(num) 
print('Sine of ',  num , ' is >>>' , result_sine)

result_log = math.log(num) 
print('Log of ' , num , ' is >>>', result_log)

result_log10 = math.log(num,10) 
print('Log of ' , num , ' with base 10 is >>>' , result_log10)
